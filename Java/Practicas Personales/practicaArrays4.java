public class practicaArrays4 {

    public static void main(String[] args) {

        int[][] array = {

                { 10, 15, 18, 19, 21 },
                { 54, 23, 5, 9, 65 },
                { 50, 45, 23, 11, 43 },
                { 70, 55, 38, 24, 17 }

        };

        for(int[]i:array){

            System.out.println();

            for(int z:i){

                System.out.print(z + " ");

            }

        }

    }

}
